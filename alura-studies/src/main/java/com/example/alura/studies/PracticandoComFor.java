package com.example.alura.studies;

public class PracticandoComFor {
    /**
     * O for é um laço de repetição onde sua estrutura permite declarar a variável dentro dela
     * Presta atenção que a variável não pode ser usada fora do for
     */
    public static void main(String[] args) {
        for(int contador = 0; contador <= 10; contador++){
            System.out.println(contador);
        }
    }
}
